def get_item_inicio(codigo_dominio, edad_meses):
    domain = ALL_BAREMOS.get(codigo_dominio)
    if not domain:
        return 1
    for regla in domain.get('reglas_inicio', []):
        if regla['min'] <= edad_meses <= regla['max']:
            return regla['inicio']
    return 1

def get_edad_equivalente(codigo_dominio, puntaje_directo):
    domain = ALL_BAREMOS.get(codigo_dominio)
    if not domain:
        return None
    edad_eq = domain.get('edad_equivalente', {}).get(puntaje_directo)
    if edad_eq is not None:
        if isinstance(edad_eq, int):
            return f"{edad_eq} meses"
        return str(edad_eq)
    return "Fuera de rango"

def get_puntaje_estandar(codigo_dominio, puntaje_directo, edad_meses):
    domain = ALL_BAREMOS.get(codigo_dominio)
    if not domain:
        return None
    
    baremos = domain.get('puntajes_estandar', [])
    for baremo in baremos:
        if baremo['min_meses'] <= edad_meses <= baremo['max_meses']:
            puntos = baremo.get('puntos', {})
            return puntos.get(puntaje_directo)
    return None

def get_percentil(puntaje_estandar):
    if puntaje_estandar is None:
        return None
    return PERCENTILES.get(puntaje_estandar)

def get_clasificacion(puntaje_estandar):
    if puntaje_estandar is None:
        return None
    for rango in INTERPRETACION:
        if rango['min'] <= puntaje_estandar <= rango['max']:
            return rango['texto']
    return None

def calcular_resultado(codigo_dominio, puntaje_directo, edad_meses):
    puntaje_estandar = get_puntaje_estandar(codigo_dominio, puntaje_directo, edad_meses)
    percentil = get_percentil(puntaje_estandar)
    edad_eq = get_edad_equivalente(codigo_dominio, puntaje_directo)
    clasificacion = get_clasificacion(puntaje_estandar)
    
    return {
        'puntaje_directo': puntaje_directo,
        'puntaje_estandar': puntaje_estandar,
        'percentil': percentil,
        'edad_equivalente': edad_eq,
        'clasificacion': clasificacion
    }

